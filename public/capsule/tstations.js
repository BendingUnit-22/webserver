    function buildGraph(svg, projection) {

	    var routes = root.slice(0, 18);

	    for (var i in routes) {

	        var line = routes[i];
		    var color = line.color;

		    var stations = svg.selectAll('.station')
                            .data(line.stations)
                            .enter()
                            .append('circle')
                            .attr('fill', function (d) {
                                d._color = color;
                                return color;
                            })
                            .attr('r', 4)
                            .attr("transform", function (d) {
                                return "translate(" + projection([d.longitude, d.latitude]) + ")";
                            });


		    stations
			    .on('mouseover',function(d) {

				    d3.select(this)
					    .transition()
					    .duration(200)
					    .attr('r', 6)
					    .attr('fill', 'yellow');
				    d3.select('#title').text(d.title);
				    d3.select('#rail').text(d.lines);
				    d3.select('#address').text(d.address);

			    })
			    .on('mouseout',function () {
				    d3.select(this)
					    .transition()
					    .duration(200)
					    .attr('r', 4)
					    .attr('fill', function (d) {
                            return d._color;
					    })



				    d3.select('#title').text("");
				    d3.select('#rail').text("");
				    d3.select('#address').text("");
			    })


	    }


    }



